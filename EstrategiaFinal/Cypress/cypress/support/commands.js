Cypress.on('uncaught:exception', (err, runnable) => {
    // esperamos el mensaje de error de ember-concurrency y lo ignoramos
    if (err.message.includes("fetchTiers' was canceled")) {
        return false;
    }

    // permitimos que Cypress falle la prueba
    return true;
});